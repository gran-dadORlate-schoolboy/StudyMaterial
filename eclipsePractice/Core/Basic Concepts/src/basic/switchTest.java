package basic;

public class switchTest {
	public static void main(String[] args) {
		switch (6) {
		case 1:
			System.out.println("1");
			break;
		case 2:
			System.out.println("2");
			break;
		case 6:
			System.out.println("6");
			break;
		case 5:
			System.out.println("5");
			break;

		default:
			break;
		}
	}

}
